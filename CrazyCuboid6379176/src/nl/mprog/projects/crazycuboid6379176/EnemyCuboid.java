package nl.mprog.projects.crazycuboid6379176;

public class EnemyCuboid
{
    int enemyCuboidW;
    int enemyCuboidH;
    int enemyCuboidX;
    int enemyCuboidY;
    
    public EnemyCuboid(int enemyCuboidW, int enemyCuboidH, int enemyCuboidX, int enemyCuboidY)
    {
        super();
        this.enemyCuboidX = enemyCuboidX;
        this.enemyCuboidY = enemyCuboidY;
        this.enemyCuboidW = enemyCuboidW;
        this.enemyCuboidH = enemyCuboidH;
    }
    
    public int getEnemyCuboidW()
    {
        return enemyCuboidW;
    }
    
    
    
    public void setEnemyCuboidW(int enemyCuboidW)
    {
        this.enemyCuboidW = enemyCuboidW;
    }
    
    
    
    public int getEnemyCuboidH()
    {
        return enemyCuboidH;
    }
    
    
    
    public void setEnemyCuboidH(int enemyCuboidH)
    {
        this.enemyCuboidH = enemyCuboidH;
    }
    
    
    
    public int getEnemyCuboidX()
    {
        return enemyCuboidX;
    }
    
    
    
    public void setEnemyCuboidX(int enemyCuboidX)
    {
        this.enemyCuboidX = enemyCuboidX;
    }
    
    
    
    public int getEnemyCuboidY()
    {
        return enemyCuboidY;
    }
    
    
    
    public void setEnemyCuboidY(int enemyCuboidY)
    {
        this.enemyCuboidY = enemyCuboidY;
    }

}
